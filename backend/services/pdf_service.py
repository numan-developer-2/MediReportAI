# ── Font Handling ─────────────────────────────────────────
def _safe_text(text: str | None, max_len: int = 5000) -> str:
    """
    Clean text for PDF generation.
    
    - Removes control characters
    - Truncates if too long
    - Handles None
    - Escapes XML-like entities
    
    Args:
        text: Input text
        max_len: Maximum length
        
    Returns:
        Safe text for PDF
    """
    if not text:
        return ""
    
    # Remove control characters (keep newlines)
    text = "".join(c for c in text if c == "\n" or ord(c) >= 32)
    
    # Escape XML special chars for ReportLab
    text = text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    
    # Normalize newlines
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    
    # Truncate
    if len(text) > max_len:
        text = text[: max_len - 3] + "..."
    
    return text


def _has_arabic_script(text: str) -> bool:
    """Check if text contains Arabic/Urdu script characters."""
    if not text:
        return False
    # Arabic script Unicode range: \u0600-\u06FF
    return bool(re.search(r"[\u0600-\u06FF]", text))


